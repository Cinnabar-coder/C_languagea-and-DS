#include<stdio.h>
// 丑数  前50个丑数为：1, 2, 3, 4, 5, 6, 8, 9, 10, 12, 15, 16, 18, 20, 24, 25, 27, 30, 32, 36。
/*如果一个数如果它能被2整除，
我们把它连续除以2；如果能被3整除，就连续除以3；如果能被5整除，就除以连续5。
如果最后我们得到的是1，那么这个数就是丑数，否则不是。*/ 
int cs (int m); //函数原型
int cs (int m) {
	while(m%2==0) {
		m/=2;
	}
	while(m%3==0) {
		m/=3;
	}
	while(m%5==0) {
		m/=5;
	}
	if(m!=1)
	{
		return 0;
	}
	return m;
}

int main() {
	int n=50;
	int i;

	for(i=1; i<=n; i++) {
	
		if(cs(i)) {
			printf("%d ",i);
		}

	}
}
